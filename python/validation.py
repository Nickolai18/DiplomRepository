def wordValidation(sentence: str):
    words = [
        "Интернет",
        "Принтер",
        "Не работает",
        "Не загружается",
        "Медленно",
        "плохо работает",
        "ЭЦП",
        "не открывается",
        "не включается",
        "нет интернета",
        "установить",
        "обновить",
        "заменить",
        "не печатает",
        "не сканирует",
    ]

    for word in words:
        if word.lower() in sentence.lower():
            return True
            break